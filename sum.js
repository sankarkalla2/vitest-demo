export function add(a, b) {
    return a + b;
}

export function subtract(a, b) {
    return a - b;
}

export function multiply(a, b) {
    return a * b;
}

export function divide(a, b) {
    return a / b;
}

export function power(a, b, c ='j') {
    return a ** b;
}


export function sqrt(a) {
    return Math.sqrt(a);
}


